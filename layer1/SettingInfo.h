union SettingInfoItem {
  int i[3];
  float f[3];
  const char * s;

  // constructors
  SettingInfoItem(int i0=0, int i1=0, int i2=0) {
    i[0] = i0;
    i[1] = i1;
    i[2] = i2;
  }
  SettingInfoItem(float f0, float f1=0.f, float f2=0.f) {
    f[0] = f0;
    f[1] = f1;
    f[2] = f2;
  }
  SettingInfoItem(const char *sval) {
    s = sval;
  }
};

